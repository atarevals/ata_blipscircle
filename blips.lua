local blips = {

--{title="Masalan 1", colour=2, id=225, x = 1014.98, y = -2333.22, z = 30.52},
--{title="Masalan 1", colour=49, id=225, x = -836.1, y = -693.7, z = 26.51},
{title="Grove", colour=2, id=310, x = -118.9, y = -1612.0, z = 31.93}

}
Citizen.CreateThread(function()

		Citizen.Wait(0)

local bool = true
  
  if bool then
        
		for k,v in pairs(blips) do
           

               zoneblip = AddBlipForRadius(v.x,v.y,v.z, 1000.0)
                          SetBlipSprite(zoneblip,1)
                          SetBlipColour(zoneblip,2)
                          SetBlipAlpha(zoneblip,150)
                         
        end
         
    
         for _, info in pairs(blips) do
        
             info.blip = AddBlipForCoord(info.x, info.y, info.z)
                         SetBlipSprite(info.blip, info.id)
                         SetBlipDisplay(info.blip, 2)
						 SetBlipScale(info.blip, 0.5)
                         SetBlipColour(info.blip, info.colour)
                         SetBlipAsShortRange(info.blip, true)
                         BeginTextCommandSetBlipName("STRING")
                         AddTextComponentString(info.title)
                         EndTextCommandSetBlipName(info.blip)
         end
	   
	   bool = false
   
   end
end)